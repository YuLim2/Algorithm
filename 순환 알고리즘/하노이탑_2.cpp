#include<stdio.h>    //표준 입출력
void hanoi(int n, char a, char b, char c){    //하노이라는 함수 생성
	if(n==1){ 
		printf("%d:%c->%c\n", n, a, c);
	} else{
		hanoi(n-1, a, c, b);
		printf("%d:%c->%c\n", n, a, c);
		hanoi(n-1, b, a, c);
	}
}

int main(){
	int n;
	scanf("%d", &n);
	hanoi(n, 'A', 'B', 'C');
	return 0;
}
